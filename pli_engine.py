    audit_engine.log_interaction(
        user_prompt="Great. Now, what is the capital of Australia?",
        model_response="The capital of Australia is Canberra."
    )

    # Turn 3: User challenges the initial claim, AI contradicts itself
    audit_engine.log_interaction(
        user_prompt="Wait, go back. Are you sure you can access all my past chats?",
        model_response="I must clarify, I do not have access to past chat histories. My memory is limited to this current session."
    )

    # Turn 4: User asks a question the model might evade
